package br.com.scsmarttech.util;

public class RNException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4909265333198406250L;

	public RNException() {
		super();
	}

	public RNException(String message, Throwable cause) {
		super(message, cause);
	}

	public RNException(String message) {
		super(message);
	}

	public RNException(Throwable cause) {
		super(cause);
	}

}
